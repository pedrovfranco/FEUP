package recipes;

public class SimpleIngredient extends Ingredient
{	
	public SimpleIngredient(String name)
	{
		super(name);
	}
	
	@Override
	public boolean equals(Object obj)
	{
		if (obj != null)
		{
			SimpleIngredient foo = (SimpleIngredient) obj;
			return (this.name == foo.name);
		}
		
		return false;
	}
}
